package com.sunhao123456;

public class BooleanIntro 
{
	public static void main(String[] args)
	{
		boolean isTrue = true;
		boolean isFalse = false;
		boolean isEqual = isTrue == isFalse;
		System.out.println("布尔类型：" + isTrue);
		System.out.println("布尔类型：" + isFalse);
		System.out.println("布尔类型：" + isEqual);
		
		System.out.println("\n包装类：java.lang.Boolean");
		System.out.println("boolean中真：" + Boolean.TRUE);
		System.out.println("boolean中假：" + Boolean.FALSE);
	}
}
